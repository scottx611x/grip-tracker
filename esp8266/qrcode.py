def display_qrcode(oled: SSD1306_I2C):
    oled.fill(0)

    scale = 2
    left = 0
    top = (64 - len(qrcode) * scale) // 2

    for y, row in enumerate(qrcode):
        for x, ch in enumerate(row):
            if ch == "1":
                oled.fill_rect(left + x * scale,
                               top + y * scale,
                               scale, scale, 1)
    oled.show()